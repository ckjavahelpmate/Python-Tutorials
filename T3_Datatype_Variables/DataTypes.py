# Python Data types
#     1. int
print( 10 )

#     2. float
print( 10.6)

#     3. str
print( "Hello" ) # Single line
print( """Hello
Sheela""" ) # Multiple line
print( 'Mal' ) # Single line
print( '''Hello
Mala''' ) # Multiple line

#     4. bool
print( True )
print( False )

#     5. complex ( includes both a real and an imaginary numbers )
z = 3 + 4j
print(z)            # (3+4j)
print(type(z))      # <class 'complex'>

# Access real and imaginary parts
print(z.real)       # 3.0
print(z.imag)       # 4.0



