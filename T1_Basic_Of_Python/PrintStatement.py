
# By default print() prints the data and moves to next line
print("Hello")
print("Sheela")

# To keep the cursor in same line after printing --> end=""
print("Hello ", end="")
print("Sheela")

# multiple data can be printed using comma(v1,v1,v3) --> v1 v2 v3
print( "Sheela", "Mala", "Leela") # Sheela Mala Leela

# We can add delimiter between multiple data using sep=""
print( "Sheela", "Mala", "Leela", sep="-") # Sheela-Mala-Leela
print( "30", "08", "2025", sep="/") # 30/08/2025
